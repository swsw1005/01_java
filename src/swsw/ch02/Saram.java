package swsw.ch02;

public class Saram {

    public void disp(int a, int b) {
        System.out.printf("a\t%d\tb\t%d\n", a, b);
        System.out.println("a+b : \t" + (a + b));
    }

    public void disp2(double a, double b) {
        System.out.printf("a\t%f\tb\t%f\n", a, b);
        System.out.println("a+b : \t" + (a + b));
    }

}