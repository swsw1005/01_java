package ch03;

// 5. boolean 연산자
// !(a) = not a
// !(!(a)) = not not a = a
public class Test05 {
    public static void main(String args[]) {

        boolean b1 = true;
        System.out.println(b1);
        System.out.println(!(b1));
        System.out.println(!(!(b1)));

    } // main end

} // class end