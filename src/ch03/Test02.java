package ch03;

// 관계연산자
public class Test02 {

    public static void main(String args[]) {

        // > 초과
        // < 미만

        // >= 이상
        // <= 미만
        // == 같다
        // != 같지않다

        int a = 10;
        int b = 3;

        System.out.println(a > b); // true
        System.out.println(a >= b); // true
        System.out.println(a < b); // false
        System.out.println(a <= b); // false
        System.out.println(a == b); // false
        System.out.println(a != b); // true

    }

}