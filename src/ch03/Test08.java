package ch03;

public class Test08 {
    public static void main(String[] args) {

        // cast 연산자
        int a1 = (int) 12.7;
        char ch = (char) 65;
        char ch2 = (char) 66.7;

        System.out.println("a1\t" + a1); // 12
        System.out.println("ch\t" + ch); // A
        System.out.println("ch2\t" + ch2); // B

    }
}